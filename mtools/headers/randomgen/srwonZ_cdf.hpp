/**
* this header file contain the cumulative distribution function for a one dimensionnal 
* simple random walk. Used to quickly simulate the law of the walk after
* 10/20/50/100/200/500/2000/5000/20000/200000/2000000 steps
*
* Code used to generate the arrays
*
*@code{.cpp}
#include "mpir.h"
#include "mpirxx.h"
#include <iomanip>

void compute(int N, std::vector<double> & tab)
{
tab.clear();
tab.reserve(N+1);
mpq_class den = 1; mpq_div_2exp(den.get_mpq_t(),den.get_mpq_t(),2*N); // den = 1/2^(2N)
double delta = 1.0/pow(10,20);
int ss = N/100;
int s = 0;
mpq_class sum = 1.0;
mpq_class C = 1; // start with C(2n,0)
for(int k = 0; k < N+1; k++)
{ // here C = C(2n,k);
mpq_class v = C*den;
if (k!= N) {v *=2;}
double p = mpq_get_d(sum.get_mpq_t());
tab[N-k] = p;
sum -= v;
C *= (2*N-k);  C /= (k+1); // next binomial coefficient this gives C(2n,k+1)
s++;  if (s >= ss) {out << "."; s=0;} // affichage de la progression
}
if (sum != 0) {out << "erreur de calcul!\n";}
}

std::string printNumber(double d)
{
std::stringstream str;
str << std::setprecision(16) << d;
return str.str();
}

// make a file with the CDF after 2*N steps
void printBinomTab(int N)
{
logger L("SRW-distr-" + tostring(2*N) + "pas.txt",false,false);
std::vector<double> res;
compute(N,res);
int l = N+1;
for(int i = 1;i<N+1;i++) {if (res[i] == res[i-1]) {l = i-1; i = N+1;}}
L << "// CDF arrray for a SRW X_n on Z after " << 2*N << " steps\n";
L << "// -> SRW_CDF_" << 2*N <<"steps[i]  = P(|X_{2n}| <= 2*k)\n";
L << "// -> the probability of getting a value strictly larger than " << 2*(l-1) << " is negligible (smaller than the double precision 10^-15)\n";
L << "const double SRW_CDF_" << 2*N << "max = " << 2*(l-1) << ";\n";
L << "const double SRW_CDF_" << 2*N << "steps[" << l << "] = {\n";
int j= 0;
for(int i = 0;i<l;i++)
{
L << printNumber(res[i]);
if (i < l-1) {L << ", ";}
j++; if (j == 5) {L << "\n"; j = 0;}
}
L << "};\n\n";
}
*@endcode
**/

